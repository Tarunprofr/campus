    public function show_record(Request $request)
    {   
        $record = Student::with('branch')->paginate(50);
        $last_page = $record->lastPage();
        $result = '';
        
        if ($request->ajax()) {
            foreach ($record as $r) {
                $status = $r->status == 1?'Active':'Inactive';
                $result .= '<tr>';
                    $result .= '<th scope="row">' . $r->id . '</th>';
                    $result .= '<td>' . $r->name . '</td>';
                    $result .= '<td>' . $r->branch->name . '</td>';
                    $result .= '<td>' . $r->class . '</td>';
                    $result .= '<td>' . $r->section . '</td>';
                    $result .= '<td>' . $r->join_date . '</td>';
                    $result .= '<td>' . $status  . '</td>';
                    $result .= '</tr>';
            }
            $data = compact('last_page','result');
            return $data;
        }
        return response()->json($record,200);
    }

